/**
 * Este paquete contiene las clases que representan las entidades y la lógica
 * del dominio del juego. Incluye clases como Player, Board, Pool, etc.
 *
 * Las clases en este paquete están diseñadas para modelar y gestionar el
 * estado y las interacciones del juego de dominó.
 *
 * Algunas clases clave en este paquete incluyen:
 * - Player: Representa a un jugador en el juego.
 * - Board: Representa el tablero de juego.
 * - Pool: Representa la piscina de fichas disponibles para el juego.
 * - TurnController: Controla el cambio de turnos entre jugadores.
 *
 * Además, hay clases específicas para representar fichas de dominó en distintos
 * contextos, como PlayerTile y PoolTile.
 *
 * Todas las clases en este paquete están diseñadas para ser serializables,
 * lo que permite la persistencia del estado del juego.
 */
