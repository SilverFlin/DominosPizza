package ui.MainMenu;

/**
 *
 * @author edemb
 */
public class AvatarDTO {
    String nombre;
    String image;

    public AvatarDTO() {
    }

    public AvatarDTO(String nombre, String image) {
        this.nombre = nombre;
        this.image = image;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
}
